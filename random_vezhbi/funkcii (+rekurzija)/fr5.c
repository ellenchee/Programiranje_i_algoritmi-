/*Да се напише рекурзивна функција која како аргумент добива две букви.
Функцијата треба да ги отпечати сите букви помеѓу тие две букви
почнувајќи од таа што е полево во азбуката кон таа што е подесно во
азбуката (абецедата).*/
#include <stdio.h>
void rek (char a, char b) {
if (a==b) printf ("%c", a);
else {
if (a>b) {
rek (a-1,b);
printf ("%c", a);}
else {
rek (a,b-1);
printf ("%c", b);}
}
}
int main (){
rek('G','B');
return 0;
}